package com.gaja.dto;

public class HideRoomDTO
{
	private int hideRCode, rcode, adminCode;
	private String hideDate, hideCause;
	
	
	public int getHideRCode()
	{
		return hideRCode;
	}
	public void setHideRCode(int hideRCode)
	{
		this.hideRCode = hideRCode;
	}
	public int getRcode()
	{
		return rcode;
	}
	public void setRcode(int rcode)
	{
		this.rcode = rcode;
	}
	public int getAdminCode()
	{
		return adminCode;
	}
	public void setAdminCode(int adminCode)
	{
		this.adminCode = adminCode;
	}
	public String getHideDate()
	{
		return hideDate;
	}
	public void setHideDate(String hideDate)
	{
		this.hideDate = hideDate;
	}
	public String getHideCause()
	{
		return hideCause;
	}
	public void setHideCause(String hideCause)
	{
		this.hideCause = hideCause;
	}
	
	
	
}
