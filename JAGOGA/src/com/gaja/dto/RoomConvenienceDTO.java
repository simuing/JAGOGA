package com.gaja.dto;

public class RoomConvenienceDTO
{
	private int rconvCode, rcode, convCode;
	private String convDate;
	
	
	public int getRconvCode()
	{
		return rconvCode;
	}
	public void setRconvCode(int rconvCode)
	{
		this.rconvCode = rconvCode;
	}
	public int getRcode()
	{
		return rcode;
	}
	public void setRcode(int rcode)
	{
		this.rcode = rcode;
	}
	public int getConvCode()
	{
		return convCode;
	}
	public void setConvCode(int convCode)
	{
		this.convCode = convCode;
	}
	public String getConvDate()
	{
		return convDate;
	}
	public void setConvDate(String convDate)
	{
		this.convDate = convDate;
	}
	
	
}
