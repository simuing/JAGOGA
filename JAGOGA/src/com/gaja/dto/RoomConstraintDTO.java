package com.gaja.dto;

public class RoomConstraintDTO
{
	private int rconsCode, rcode, consCode;
	private String consDate;
	
	
	public int getRconsCode()
	{
		return rconsCode;
	}
	public void setRconsCode(int rconsCode)
	{
		this.rconsCode = rconsCode;
	}
	public int getRcode()
	{
		return rcode;
	}
	public void setRcode(int rcode)
	{
		this.rcode = rcode;
	}
	public int getConsCode()
	{
		return consCode;
	}
	public void setConsCode(int consCode)
	{
		this.consCode = consCode;
	}
	public String getConsDate()
	{
		return consDate;
	}
	public void setConsDate(String consDate)
	{
		this.consDate = consDate;
	}
	
	
}
