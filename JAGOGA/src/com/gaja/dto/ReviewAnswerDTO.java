public class ReviewAnswerDTO
{
	private int revAnsCode, revCode;
	private String revAns, revAnsDate;
	
	
	public int getRevAnsCode()
	{
		return revAnsCode;
	}
	public void setRevAnsCode(int revAnsCode)
	{
		this.revAnsCode = revAnsCode;
	}
	public int getRevCode()
	{
		return revCode;
	}
	public void setRevCode(int revCode)
	{
		this.revCode = revCode;
	}
	public String getRevAns()
	{
		return revAns;
	}
	public void setRevAns(String revAns)
	{
		this.revAns = revAns;
	}
	public String getRevAnsDate()
	{
		return revAnsDate;
	}
	public void setRevAnsDate(String revAnsDate)
	{
		this.revAnsDate = revAnsDate;
	}
	
	
}
