package com.gaja.dto;

public class ReqVerificationDTO
{
	private int reqvCode, rcode;
	private String reqvAttach;
	
	
	public int getReqvCode()
	{
		return reqvCode;
	}
	public void setReqvCode(int reqvCode)
	{
		this.reqvCode = reqvCode;
	}
	public int getRcode()
	{
		return rcode;
	}
	public void setRcode(int rcode)
	{
		this.rcode = rcode;
	}
	public String getReqvAttach()
	{
		return reqvAttach;
	}
	public void setReqvAttach(String reqvAttach)
	{
		this.reqvAttach = reqvAttach;
	}
	
	
}
