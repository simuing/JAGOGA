package com.gaja.dto;

public class ReviewDTO
{
	private int revCode, ulistCode;
	private String revTitle, revContent, regDate;
	public int getRevCode()
	{
		return revCode;
	}
	public void setRevCode(int revCode)
	{
		this.revCode = revCode;
	}
	public int getUlistCode()
	{
		return ulistCode;
	}
	public void setUlistCode(int ulistCode)
	{
		this.ulistCode = ulistCode;
	}
	public String getRevTitle()
	{
		return revTitle;
	}
	public void setRevTitle(String revTitle)
	{
		this.revTitle = revTitle;
	}
	public String getRevContent()
	{
		return revContent;
	}
	public void setRevContent(String revContent)
	{
		this.revContent = revContent;
	}
	public String getRegDate()
	{
		return regDate;
	}
	public void setRegDate(String regDate)
	{
		this.regDate = regDate;
	}
	
	
}
