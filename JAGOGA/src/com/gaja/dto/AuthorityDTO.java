package com.gaja.dto;

public class AuthorityDTO
{
	private int atCode;
	private String atName, atContent;
	
	
	public int getAtCode()
	{
		return atCode;
	}
	public void setAtCode(int atCode)
	{
		this.atCode = atCode;
	}
	public String getAtName()
	{
		return atName;
	}
	public void setAtName(String atName)
	{
		this.atName = atName;
	}
	public String getAtContent()
	{
		return atContent;
	}
	public void setAtContent(String atContent)
	{
		this.atContent = atContent;
	}
	
	
}
