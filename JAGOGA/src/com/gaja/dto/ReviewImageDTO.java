package com.gaja.dto;

public class ReviewImageDTO
{
	private int revImgCode, revCode;
	private String revImg;
	
	
	public int getRevImgCode()
	{
		return revImgCode;
	}
	public void setRevImgCode(int revImgCode)
	{
		this.revImgCode = revImgCode;
	}
	public int getRevCode()
	{
		return revCode;
	}
	public void setRevCode(int revCode)
	{
		this.revCode = revCode;
	}
	public String getRevImg()
	{
		return revImg;
	}
	public void setRevImg(String revImg)
	{
		this.revImg = revImg;
	}
	
	
}
